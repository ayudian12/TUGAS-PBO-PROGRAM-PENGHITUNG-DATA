public class PenghitungJava {
        public int getPenghitungPengurangan(int O, int P){
            int pengurangan =O + P;
            return pengurangan;
            
        }
            
        public int getPenghitungPenjumlahan (int A, int B){
            int Penjumlahan  = A + B;
            return Penjumlahan ;
            
        }
        
        public int getPenghitungPerkalian(int R, int F){
            int Perkalian = R * F;
            return Perkalian;
            
        }
        
        public int getPenghitungPembagian(int F, int G){
            int Pembagian = F / G;
            return Pembagian;
            
        }
}    